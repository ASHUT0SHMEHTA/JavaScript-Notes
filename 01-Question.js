// Q1. What is JavaScript? What is the role of JavaScript engine?

// JavaScript is a high-level, interpreted programming language primarily used for web development. It allows developers to make web pages interactive, handle events, manipulate HTML/CSS, and communicate with servers.

// Key points:

// It is client-side by default, but can also run on the server (Node.js).

// It is dynamically typed: variable types are determined at runtime.

// It is multi-paradigm: supports object-oriented, functional, and procedural programming.

// Example of JavaScript:

// // A simple JavaScript program

let name = "Ashutosh";
console.log("Hello, " + name + "!");

// Output:

// Hello, Ashutosh!

// Role of JavaScript engine

// A JavaScript Engine is a program that executes JavaScript code. Every modern browser has its own engine:

// Browser                      JS Engine

// Chrome                          V8
// Firefox                     SpiderMonkey
// Safari                   JavaScriptCore (Nitro)
// Edge                          Chakra

// Functions of JS Engine:

// 1. Parses the JavaScript code.

// 2. Compiles or interprets the code into machine code.

// 3. Executes the code.

// 4. Optimizes performance for faster execution.

// Example (inside Chrome using V8 engine):

// When you write console.log("Hello"), V8 parses it, converts it into machine instructions, and executes it.

// Summary for notes:

// JavaScript: Language to make websites interactive.

// JS Engine: Executes JS code (parses - compiles - runs - optimizes).
